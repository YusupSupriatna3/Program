                <h2>Praktik Kerja Industri (Prakerin) adalah .....</h2>
                <p>Praktik Kerja Industri (Prakerin) adalah bagian dari Pendidikan Sistem Ganda (PSG) sebagai program bersama antara Sekolah Menengah Kejuruan (SMK) dan Industri yang dilaksanakan di dunia usaha dan dunia industri. Dalam Kurikulum SMK (Dikmenjur, 2008) disebutkan: “Prakerin adalah pola penyelenggaraan diklat yang dikelola bersama-sama antara SMK dengan industri atau  asosiasi profesi sebagai institusi pasangan (IP), mulai dari tahap perencanaan, pelaksanaan hingga evaluasi dan sertifikasi yang merupakan satu kesatuan program dengan menggunakan berbagai bentuk alternatif pelaksanaan, seperti day release, block release, dan sebagainya”.</p>
                <p>Kemudian dalam jurnal program Prakerin (1999) dalam Muhidin (2010) dijelaskan bahwa Prakerin adalah suatu komponen praktik keahlian profesi, berupa kegiatan secara terprogram dalam situasi sebenarnya untuk mencapai tingkat keahlian dan sikap kerja profesional yang dilakukan di industri.</p>
                <p>Pembelajaran di dunia kerja (industri) tersebut merupakan bagian integral dari program diklat secara menyeluruh, karena itu materi yang dipelajari dan kompetensi yang dilatihkan harus jelas kaitannya dengan profil kompetensi tamatan yang telah ditetapkan. Program diklat disusun dan dilaksanakan bersama secara bertanggungjawab antara sekolah dan industri, serta didukung oleh Kamar Dagang dan Industri (KADIN) mewakili industri dan tokoh masyarakat yang mewakili masyarakat umum (Muhidin, 2010).</p>
                <p>Lebih lanjut dalam Undang-Undang Prakerin Dikmendikti (2003) diungkapkan bahwa Praktik Kerja Industri (Prakerin) adalah program wajib yang harus diselenggarakan oleh sekolah khususnya Sekolah Menengah Kejuruan (SMK) dan pendidikan luar sekolah serta wajib diikuti oleh siswa/warga belajar. Penyelenggaraan Praktik Kerja Industri akan membantu peserta didik untuk memantapkan hasil belajar yang diperoleh di sekolah serta membekali siswa dengan pengalamn nyata sesuai dengan program studi yang dipilihnya.</p>
